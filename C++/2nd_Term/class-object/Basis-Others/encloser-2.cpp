// 封闭类的复制构造函数
// 封闭类的复制构造过程，会调用它内部所包含的每个成员对象的复制构造函数。（理解这一点）
// 封闭类的复制构造过程，会调用它所包含的每一个成员对象（以及基类子对象）的复制构造函数。
// 这是 C++ 实现“深拷贝”语义的基础，保证了对象成员的独立复制。
#include <iostream>
using namespace std;

class A {
public:
    A() { cout << "default" << endl; }
    A(const A & a) { cout << "copy" << endl; }
};

class B {
    A a;   // B 包含一个 A 类型的成员对象
};

int main() {
    B b1;        
    // (1)
    // 语句 B b1;调用 B 的合成默认构造函数。该构造函数会调用 A::A() 来初始化 b1.a。
    // 输出第一行：default
    B b2(b1);    
    // (2)
    // 语句 B b2(b1);调用 B 的合成拷贝构造函数。
    // 该构造函数会调用 A::A(A &) 来初始化 b2.a，实参是 b1.a（非常量左值，正好匹配 A(A &) 的参数类型）。
    // 输出第二行：copy
    return 0;
}