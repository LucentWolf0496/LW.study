// 指针和引用的根本区别：
// 指针：存放变量地址的变量，可以在定义时不初始化（但建议初始化），之后可以改变指向。
// 引用：是变量的别名，定义时必须初始化，且一旦绑定，不能再改变为其他变量的引用。
// 其他区别：
// 1.是否可以为空
// 指针：可以赋值为 nullptr，表示不指向任何对象。使用前需判空。
// 引用：不能为空，必须引用一个已经存在的对象。
// 2.内存占用与地址
// 指针：是一个变量，本身占用内存（通常4或8字节），存储的是地址。可以获取指针自身的地址。
// 引用：不占用额外内存，它只是原变量的别名。对引用取地址，得到的是原变量的地址。
// 3.重载与运算符
// 指针：可以进行指针运算（如 ++、--、+ 等），用于数组遍历。
// 引用：不支持运算，它只是别名。

// 没有引用的引用，也没有引用的指针（但可以有指针的引用）。
// 定义引用时一定要将其初始化成引用某个变量。
// 初始化后，它就一直引用该变量，不会再引用别的变量了。
// 引用只能引用变量，不能引用常量和表达式。
// 错误：int n = 4;	int & r = n * 5; ————不能引用表达式

// 定义引用时，前面加const关键字，即为“常引用”
// 不能通过常引用去修改其引用的内容，仅可以直接修改被引用的内容，常引用将修改途径给锁死了
// 不可通过常量指针修改其指向的内容，但常量指针的指向可以变化
// 区分：引用为&，指针为*

// const T & 和T & 是不同的类型!!!
// T & 类型的引用或T类型的变量可以用来初始化const T & 类型的引用。
// const T 类型的常变量和const T & 类型的引用则不能用来初始化T &类型的引用，除非进行强制类型转换。
// 不能把常量指针赋值给非常量指针，除非进行强制类型转换const int * p1; int * p2; p2 = (int * ) p1; 反过来可以
// 错误：int n = 8;	const int & r1 = n;  int & r2 = r1;————不能用const去初始化一般引用
// 正确：int n = 8;	int & r1 = n;	const int r2 = r1; ————可以用一般引用去初始化const

#include <iostream>
using namespace std;

int main(){
    int a = 4;
    int b = 5;
    int& r1 = a;
    int& r2 = r1;
    r2 = 10;// r2也引用a
    cout << a << endl;// 输出10
    r1 = b;// r1并没有引用b，只是修改值为b 
    cout << a << endl;// 输出5
}
